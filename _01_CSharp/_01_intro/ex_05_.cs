using System;

class VariablesAndTypes 
{
  public static void Main(string[] args)
  {
    int age = 21;
    Console.WriteLine(age);

    bool approved = true;
    Console.WriteLine(approved);

    char c = 'z';
    Console.WriteLine(c);

    string name = "Evil";
    Console.WriteLine(name);

    double height = 1.95;
    Console.WriteLine(height); 
  }
} 